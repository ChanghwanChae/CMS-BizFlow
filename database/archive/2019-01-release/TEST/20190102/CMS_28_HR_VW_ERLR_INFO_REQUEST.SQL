CREATE OR REPLACE VIEW VW_ERLR_INFO_REQUEST
AS
SELECT
    IR.ERLR_CASE_NUMBER
    , EC.ERLR_JOB_REQ_NUMBER
    , EC.PROCID    
    , EC.ERLR_CASE_CREATE_DT
    , IR_REQUESTER
	, IR_CMS_REQUESTER_NAME
	, IR_CMS_REQUESTER_PHONE
	, IR_NCMS_REQUESTER_LAST_NAME
	, IR_NCMS_REQUESTER_FIRST_NAME
	, IR_NON_CMS_REQUESTER_PHONE
	, IR_NON_CMS_REQUESTER_EMAIL
	, IR_NCMS_REQUESTER_ORG_AFFIL
	, IR_SUBMIT_DT
	, CASE WHEN IR_MEET_PTCLRIZED_NEED_STND = '1'  THEN 'Yes' ELSE 'No' END AS IR_MEET_PTCLRIZED_NEED_STND
	, CASE WHEN IR_RSNABLY_AVAIL_N_NECESSARY = '1'  THEN 'Yes' ELSE 'No' END AS IR_RSNABLY_AVAIL_N_NECESSARY
	, CASE WHEN IR_PRTCT_DISCLOSURE_BY_LAW = '1'  THEN 'Yes' ELSE 'No' END AS IR_PRTCT_DISCLOSURE_BY_LAW
	, CASE WHEN IR_MAINTAINED_BY_AGENCY = '1'  THEN 'Yes' ELSE 'No' END AS IR_MAINTAINED_BY_AGENCY
	, CASE WHEN IR_COLLECTIVE_BARGAINING_UNIT = '1'  THEN 'Yes' ELSE 'No' END AS IR_COLLECTIVE_BARGAINING_UNIT
	, CASE WHEN IR_APPROVE = '1'  THEN 'Yes' ELSE 'No' END AS IR_APPROVE
	, CASE WHEN IR_APPEAL_DENIAL = '1'  THEN 'Yes' ELSE 'No' END AS IR_APPEAL_DENIAL

FROM
	ERLR_INFO_REQUEST IR
    LEFT OUTER JOIN ERLR_CASE EC ON IR.ERLR_CASE_NUMBER = EC.ERLR_CASE_NUMBER
;
